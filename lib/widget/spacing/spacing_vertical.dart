import 'package:flutter/material.dart';

Widget SpacingVertical(double height) {
  return SizedBox(
    height: height,
  );
}

Widget SpacingHorizontal(double width) {
  return SizedBox(
    width: width,
  );
}

Widget SpacingBox(double height, double width) {
  return SizedBox(
    height: height,
    width: width,
  );
}
